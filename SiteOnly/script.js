function build2027EventMap() {
    const map = {};
    calendarData.forEach(entry => {
        const key = `${entry.month}-${entry.day}`;
        if (!map[key]) map[key] = [];
        map[key].push(entry);
    });
    return map;
}

function render2027Calendar() {
    const container = document.getElementById('events-2027-grid');
    if (!container) return;

    const eventMap2027 = build2027EventMap();
    const year = 2027;
    let html = '';

    for (let m = 0; m < 12; m++) {
        const firstDay = new Date(year, m, 1);
        const daysInMonth = new Date(year, m + 1, 0).getDate();
        let startDay = firstDay.getDay() - 1;
        if (startDay < 0) startDay = 6;

        html += `<div class="cal27-month">`;
        html += `<div class="cal27-month-header">${monthNames[m]} ${year}</div>`;
        html += `<div class="cal27-days">`;

        const weekdays = ['Пон', 'Вт', 'Ср', 'Чет', 'Пет', 'Съб', 'Нед'];
        weekdays.forEach(w => { html += `<div class="cal27-weekday">${w}</div>`; });

        for (let i = 0; i < startDay; i++) {
            html += '<div class="cal27-day empty"></div>';
        }

        for (let day = 1; day <= daysInMonth; day++) {
            const key = `${m}-${day}`;
            const entries = eventMap2027[key] || [];
            const hasEvent = entries.length > 0;
            const cats = [...new Set(entries.map(e => e.category))];
            let dots = '';
            if (hasEvent) {
                dots = '<div class="cal27-dots">' + cats.map(c => `<span class="cal27-dot cat-${c}"></span>`).join('') + '</div>';
            }
            const dataAttrs = hasEvent ? `data-event-ids="${entries.map(e=>e.id).join(',')}"` : '';
            html += `<div class="cal27-day${hasEvent ? ' has-event' : ''}" ${dataAttrs}><span class="cal27-day-num">${day}</span>${dots}</div>`;
        }

        html += `</div></div>`;
    }

    container.innerHTML = html;

    container.querySelectorAll('.cal27-day.has-event').forEach(dayEl => {
        dayEl.addEventListener('click', () => {
            const ids = dayEl.dataset.eventIds.split(',').map(Number);
            const entries = ids.map(id => calendarData.find(e => e.id === id)).filter(Boolean);
            if (entries.length === 1) {
                openEventModal(entries[0]);
            } else if (entries.length > 1) {
                openEventModal(entries[0]);
            }
        });
    });
}

