let activeFilter = 'all';

function renderTimeline(filter) {
    const list = document.getElementById('timeline-list');

    const sorted = [...calendarData].sort((a, b) => {
        if (a.year !== b.year) return a.year - b.year;
        if (a.month !== b.month) return a.month - b.month;
        return a.day - b.day;
    });

    const filtered = filter === 'all' ? sorted : sorted.filter(e => e.category === filter);

    const groups = {};
    filtered.forEach(entry => {
        if (!groups[entry.year]) groups[entry.year] = [];
        groups[entry.year].push(entry);
    });

    let html = '';
    Object.keys(groups).sort((a, b) => a - b).forEach(year => {
        html += `<div class="tl-year-group" data-year="${year}">`;
        html += `<div class="tl-year-label"><span class="tl-year-text">${year} г.</span></div>`;
        groups[year].forEach(entry => {
            const catClass = getCategoryColorClass(entry.category);
            const dayLabel = entry.day > 1
                ? `${entry.day} ${monthNames[entry.month]} ${entry.year}`
                : `${monthNames[entry.month]} ${entry.year}`;
            html += `
                <div class="tl-card ${catClass}" data-event-id="${entry.id}">
                    <div class="tl-card-thumb">
                        <img src="${entry.photo}" alt="${entry.personName}" loading="lazy">
                    </div>
                    <div class="tl-card-body">
                        <div class="tl-card-name">${entry.personName}</div>
                        <div class="tl-card-meta">
                            <span class="tl-card-date">${dayLabel}</span>
                            <span class="tl-card-badge ${catClass}">${getTypeSymbol(entry.type)} ${getTypeLabel(entry.type)} — ${getCategoryLabel(entry.category)}</span>
                        </div>
                        <div class="tl-card-desc">${entry.shortDesc}</div>
                    </div>
                </div>
            `;
        });
        html += `</div>`;
    });

    list.innerHTML = html;

    list.querySelectorAll('.tl-card').forEach(card => {
        card.addEventListener('click', () => {
            const id = parseInt(card.dataset.eventId);
            const entry = calendarData.find(e => e.id === id);
            if (entry) openEventModal(entry);
        });
    });
}

