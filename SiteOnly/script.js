function renderMiniCalendar(year, month, highlightDay) {
    const grid = document.getElementById('mini-calendar-grid');
    const title = document.getElementById('mini-calendar-title');
    title.textContent = `${monthNames[month]} ${year}`;

    const firstDay = new Date(year, month, 1);
    const daysInMonth = new Date(year, month + 1, 0).getDate();
    let startDay = firstDay.getDay() - 1;
    if (startDay < 0) startDay = 6;

    let html = '';
    ['Пон','Вт','Ср','Чет','Пет','Съб','Нед'].forEach(w => {
        html += `<div class="mini-calendar-weekday">${w}</div>`;
    });
    for (let i = 0; i < startDay; i++) {
        html += '<div class="mini-calendar-day empty">·</div>';
    }
    for (let day = 1; day <= daysInMonth; day++) {
        html += `<div class="mini-calendar-day${day === highlightDay ? ' highlighted' : ''}">${day}</div>`;
    }
    grid.innerHTML = html;
}

function openEventModal(entry) {
    const overlay = document.getElementById('modal-overlay');
    const dayView = document.getElementById('modal-day-view');
    const detailView = document.getElementById('modal-event-detail');

    document.getElementById('modal-title').textContent = entry.personName;
    const badgeEl = document.getElementById('modal-type-badge');
    badgeEl.textContent = `${getTypeSymbol(entry.type)} ${getTypeLabel(entry.type)} — ${getCategoryLabel(entry.category)}`;
    badgeEl.className = `modal-type-badge ${getCategoryColorClass(entry.category)}`;

    const imageEl = document.getElementById('modal-image');
    imageEl.src = entry.photo;
    imageEl.alt = entry.personName;
    imageEl.style.display = 'block';

    document.getElementById('modal-short-desc').textContent = entry.shortDesc;
    document.getElementById('modal-details').textContent = entry.details;

    renderMiniCalendar(entry.year, entry.month, entry.day);

    dayView.style.display = 'none';
    detailView.style.display = 'block';
    overlay.classList.add('active');
    document.body.style.overflow = 'hidden';
}

function closeModal() {
    document.getElementById('modal-overlay').classList.remove('active');
    document.body.style.overflow = '';
}

